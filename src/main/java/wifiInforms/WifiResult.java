package wifiInforms;

public class WifiResult {
	private String CODE;  // 실행 결과 코드
	private String MESSAGE; // 실행 결과 메시지
	
	

	public String getCODE() {
		return CODE;
	}
	public void setCODE(String cODE) {
		CODE = cODE;
	}
	public String getMESSAGE() {
		return MESSAGE;
	}
	public void setMESSAGE(String mESSAGE) {
		MESSAGE = mESSAGE;
	}
	
}
